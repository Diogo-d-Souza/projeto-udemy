def add(n1, n2):
    return n1 + n2

def substract (n1, n2):
    return n1 - n2

def multiply(n1, n2):
    return n1 * n2

def divide(n1, n2):
    return n1 / n2

operations = {
    "+": add,
    "-": substract,
    "*": multiply,
    "/": divide
}


def calculator():
    print(logo)
    num1 = float(input("What's the first number?: "))
    for op in operations:
        print(op)
    end_calculator = True

    while end_calculator:

        symbol = input("Pick an symbol operator: ")
        num2 = float(input("What's the second number?: "))
        result = operations[symbol](num1,num2)
        print("{} {} {} = {}".format(num1, symbol, num2, result))

        progression = input("Do you want calculating {}, type 'y' or type 'n' to restart calculator.\n".format(result))
        if progression.lower() == "y":
            num1 = result
        else:
            end_calculator = False
            calculator()

calculator()